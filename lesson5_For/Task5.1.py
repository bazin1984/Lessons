for i in range(4) :  # range создает диапазон перебора переменной i. range работает только с числами
    print(i)
# range(4,8)  - формирует диапазон от 4 до 8б не включая 8
# range(4,8,1) - 1 это шаг может быть отрицательным
# range(4,8,1) = (4,5,6,7)
# range(1,9,3) = (1,4,7)
# range(10,5,-2) = (10,8,6)

